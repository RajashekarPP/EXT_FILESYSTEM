#ifndef __INCLUDE_TYPES_H__
#define __INCLUDE_TYPES_H__

typedef signed char __le8;
typedef unsigned char __ule8;
typedef signed short __le16;
typedef unsigned short __ule16;
typedef signed int __le32;
typedef unsigned int __ule32;
typedef signed long long __le64;
typedef unsigned long long __ule64;
typedef float __lefl;

#endif
